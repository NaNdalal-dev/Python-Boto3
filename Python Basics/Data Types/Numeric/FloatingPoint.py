"""
->  Used for decimal or floating-point numbers.
->  Supports scientific notation (e for exponent).
"""

a = 3.14  
b = -0.001  
c = 2.5e3  # 2.5 × 10³ = 2500 

# Printing Values of a, b, c
print("Value of a:",a)
print("Value of b:",b)
print("Value of c:",c)

#Check the type
print(type(a))  # Output: <class 'float'>
print(type(b))  # Output: <class 'float'>
print(type(c))  # Output: <class 'float'>


